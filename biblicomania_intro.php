          <h3>LA PALABRA DE DIOS</h3>
         <!-- <img src="images/page2_img1.jpg" alt="" class="img_inner fleft">-->
          <p class="textContent">“Prudencia: ¿Qué piensas de la Biblia? </p>
          <p class="textContent">Mateo: Que es la Santa Palabra de Dios.”<a title="BUNYAN, John. “The second part of the pilgrim’s progress”. Oxford University Press. Gran Bretaña. 2003. Pág. 213."><span class="textpie">(1)</span> </a></p>
          <p class="textContenido">Con este sencillo diálogo, el famoso puritano<a title="Puritanismo: Doctrina política y religiosa que apareció en tiempos de Eduardo VI de Inglaterra a mediados del siglo XVI. Los puritanos pertenecen al grupo de las llamadas iglesias no conformistas y propugnan la supresión de la mayoría del aparato religioso, la ostentación y el lujo del culto, la abolición de la jerarquía episcopal, etc. Los puritanos aceptaban los postulados de Calvino y admiten la interpretación individual de los textos sagrados. En política son republicanos y demócratas, sosteniendo el principio de igualdad con la misma energía que en religión. Enciclopedia ilustrada cumbre. Tomo diez. Quinta edición. Editorial Cumbre S.A. de C.V. México. 1965. Pág.338. "><span class="textpie">(2) </span></a>John Bunyan resumía una de las creencias fundamentales de todo verdadero cristiano, ya que a lo largo de los siglos, iniciando por los cristianos de los siglos I, II y III, pasando por los reformadores como Martín Lutero del siglo XVI, los grandes predicadores como el mismo Bunyan del siglo XVII, y Jonathan Edwards, John Wesley y Geroge Whitefield del siglo XVIII, hasta llegar al siglo XXI; todos los genuinos creyentes de Jesucristo han sostenido que la Biblia es la Santa Palabra de Dios.<br/><br/>

La misma Escritura ha sido el fundamento para afirmar que es la viva voz de Dios hablando a los hombres, como prueba de esto podemos mencionar que tan solo en Antiguo Testamento declara mas de dos mil veces ser la Palabra de Dios.<a title="MACARTHUR, John. “La Biblia de Estudio MacArthur. Cómo obtuvimos la Biblia. Portavoz.  Grand Rapids, Michigan, USA. 1997. Pág. 15."><span class="textpie">(3) </span></a><br/><br/>

La Biblia durante los siglos ha sido el fundamento esencial de la fe y de la vida cristiana como lo expone Martín Lutero en su obra “Sobre la libertad de un cristiano” donde afirma lo siguiente: “Quinto, el alma no tiene otra cosa, con la que pueda vivir piadosa, libre y cristiana, más que el santo Evangelio, la Palabra de Dios, predicada por Cristo, como Juan 11 (25) dice: “Yo soy la resurrección y la vida; el que cree en mi, , vivirá eternamente” Item 14 (6) “Yo soy el camino, la verdad y la vida.” Entonces debemos estar seguros, de que el alma puede prescindir de todas las cosas excepto de la Palabra de Dios, y sin la Palabra de Dios no puede ser ayudada con ninguna otra cosa. En cambio cuando tiene la Palabra de Dios, no necesita más de ninguna otra cosa, sino que en la Palabra tiene satisfacción, alimento, felicidad, paz, luz, entendimiento, justicia, verdad, sabiduría, y todo bien en abundancia.”<a title="LUTHER, Martin. “Von der Freiheit eines Christenmenschen”. Editorial Reclam. Stuttgart. 1962. Págs. 126.127."><span class="textpie">(4) </span></a><br/<br/>
<br/>
Es debido a este papel fundamental que desempeña la Escritura en la vida cristiana, que hemos decidido elaborar un pequeño estudio sobre su historia, el cual, con la ayuda de Dios, nos permitirá entender el largo proceso por el que tuvo que pasar la Palabra de Dios para llegar a nosotros. </p>
<p class="texttitle">EL CANON DE LAS ESCRITURAS </p>
       <p class="textContenido">   Para iniciar este apartado es necesario en primer término definir la palabra “canon”.
<br/><br/>
Canon: (caña, regla) en sentido literal es cualquier regla o vara que sirva para medir. En sentido figurado esta palabra tiene varias connotaciones, para efectos de este trabajo tomaremos la siguiente: Lista normativa de libros inspirados y recibidos de parte de Dios.<a title="VILA VENTURA, Samuel y ESCUAÍN, Santiago. “Nuevo Diccionario Bíblico Ilustrado”. Editorial Clie. Barcelona, España. 1985. Pág. 135."><span class="textpie">(5)</span></a><br/><br/>

Para comprender el proceso de canonización es necesario entender que la Biblia es un libro con un Autor divino, el cual, se escribió en un periodo de aproximadamente 1500 años<a title="libro de Génesis se escribió aproximadamente en el siglo XIV antes de Cristo, mientras que el libro de Apocalipsis se escribió entre los años 94 y 96 después de Cristo."><span class="textpie">(6)</span></a>, por medio de casi 40 autores, mismos que provenían de contextos históricos, económicos y sociales distintos. Durante este largo periodo de tiempo Dios se fue revelando a los hombres por medio de las Escrituras inspiradas, mismas que se fueron recopilando conforme iban apareciendo.
<br/><br/>
Para poder identificar entre las Escrituras verdaderamente inspiradas por Dios, y las falsas Escrituras, se emplearon tres principios básicos:
<br/><br/>
1. El escrito debía de tener a un profeta o a un apóstol reconocido (o un asociado de ellos) como autor.<br/>
2. El escrito no podía estar en desacuerdo o contradecir las Escrituras previamente reveladas.<br/>
3. El escrito necesitaba de un consenso general por parte de la Iglesia como un libro revelado.<a title="MACARTHUR, John. “La Biblia de Estudio MacArthur. Cómo obtuvimos la Biblia. Portavoz.  Grand Rapids, Michigan, USA. 1997. Págs. 16 y 17."><span class="textpie">(7)</span></a>
<br/><br/>
Efectivamente los libros falsos siempre fueron una amenaza latente para el pueblo de Dios, por lo cual, era de suma importancia revisar cuidadosamente la colección de los libros inspirados.<br/><br/>

A pesar de esta apremiante necesidad, es importante entender como lo señala Ralph Mahoney, que no se debe de imaginar a un comité de los líderes de la Iglesia con una larga hilera de libros esperando a ser evaluados en base a los principios rectores ya mencionados, Mahoney menciona que el proceso de canonización fue mucho más natural y dinámico.<a title="MAHONEY, Ralph. “El cayado del pastor” (Sección C1). World Map. Burbank, Clalifornia, USA. 1995. Pág. 4."><span class="textpie">(8)</span></a><br/><br/>

Ahora, recordemos que la Biblia se divide en dos grandes secciones, el Antiguo y el Nuevo Testamento, y cada una de estas secciones pasó por su propio proceso de canonización. A continuación analizaremos brevemente el proceso de cada sección.
<br/><br/>
La misma Biblia nos ilustra como fue el proceso de canonización del Antiguo Testamento, el Doctor Samuel Vila Ventura en su Diccionario Bíblico Ilustrado hace el siguiente resumen:
<br/><br/><em>
“Los documento literarios con autoridad en Israel se multiplicaron poco a poco, y fueron celosamente conservados. Tenemos ejemplos de esta redacción de los libros santos. La ley fundamental de los diez mandamientos escritos sobre tablas de piedra fue depositada dentro del arca (Ex. 40:20). Estos estatutos figuran en el libro del pacto (Ex. 20:23, 23:33; 24:7). El libro de la Ley, redactado por Moisés, fue guardado al lado del arca (Dt. 31:24-26). Josué adjuntó lo que él había escrito (Jos. 24:26). Samuel consignó el derecho de los reyes en un libro que puso ante el Señor (1.S. 10:25). Bajo Josías, se encontró, durante las obras de restauración del templo, el libro de la Ley de Jehová. El rey, los sacerdotes, los profetas, y el pueblo reconocieron su autoridad y antigüedad (2. R. 22:8-20); se hicieron copias de esta ley, según la orden dada ya en Dt. 17:18-20. Los profetas dejaron sus propias palabras (p. Ej., Jer. 36:32), tomaban nota recíproca, y las citaban como autoridades (Esd. 2:2-4; cp. Mi. 4:1-3). Se reconocía la autoridad de la ley y de las palabras de los profetas, escritos inspirados por el Espíritu Santo de Dios, y celosamente preservados por Jehová (Zac. 1:4; 7:7, 12).”</em> <a title="VILA VENTURA, Samuel y ESCUAÍN, Santiago. “Nuevo Diccionario Bíblico Ilustrado”. Editorial Clie. Barcelona, España. 1985. Pág. 135."><span class="textpie">(9)</span></a>
<br/><br/>
La elaboración del Antiguo Testamento abarcó un largo periodo de tiempo de casi mil años, ya que comienza con la redacción el Pentateuco en el siglo XIV a. C., y termina con el libro de Malaquías, el cual, fue concluido entre los años 433 y 432 a. C.. Fue dentro de este lapso que aparecieron los 39 libros que constituyen al Antiguo testamento.
<br/><br/>
Con lo anterior encontramos que el Antiguo Testamento es la porción de la Biblia con el periodo temporal de redacción mas largo, con la mayor cantidad de libros, y con la mayor cantidad de autores. Por consiguiente estimamos prudente hacer una pequeña referencia en relación a los libros del Antiguo Testamento y sus autores. </p>
