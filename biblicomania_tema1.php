          <p class="texttitle">El PENTATEUCO </p>
          <p class="textContenido">Pentateuco es el nombre que se le da a los primeros cinco libros de Antiguo Testamento, Génesis, Éxodo, Levítico, Números y Deuteronomio, el cual, debió de haber sido redactado entre los años 1445 y 1405 antes de Cristo. <br><br>Es importante señalar que la palabra “Pentateuco” no se encuentra en las escrituras, los israelitas solían llamar a este conjunto de libros con los siguientes términos: “la Ley”, “La Ley de Moisés”, “La Ley de Jehová”, “Libro de la Ley de Dios”, entre otros. Estas expresiones nos llevan a pensar que originalmente los cinco libros formaban uno solo. <br><br>El historiador judío Flavio Josefo menciona que la división en cinco libros probablemente se dio por la traducción del Antiguo Testamento al griego, empresa llevada a cabo por 72 judíos eruditos, la cual, fue concluida aproximadamente en el año 150 antes de Cristo, dando así origen a la versión de los LXX o Septuaginta.<a title="VILA VENTURA, Samuel y ESCUAÍN, Santiago. “Nuevo Diccionario Bíblico Ilustrado”. Editorial Clie. Barcelona, España. 1985. Págs. 906 y 1139."><span class="textpie">(1)</span></a><p></p>

<p class="texttitle">EVIDENCIAS QUE NOS PRUEBAN QUE MOISÉS ES EL AUTOR DEL PENTATEUCO: </p>
          <p class="incisos">1. Evidencias internas:</p>
<p class="textContenido">
El mismo Pentateuco menciona de manera expresa que Moisés es su autor. <br>Encontramos prueba de esto en los siguientes pasajes:<br><br>
Números 33:2 <br><em>"Moisés escribió sus salidas conforme a sus jornadas por mandato de Jehová. Estas, pues, son sus jornadas con arreglo a sus salídas."</em><br/><br/>
Éxodo 17:14 <br/><em>"Y Jehová dijo a Moisés: Escribe esto para memoria en un libro, y di a Josué que raeré del todo la memoria de Amalec de debajo del cielo.</em>”<br/><br/>
Éxodo 15: 1-21 es un cántico elaborado por Moisés y María su hermana.<br/><br/>
Éxodo 24: 4 <em>"Y Moisés escribió todas las palabras de Jehová, y levantándose de mañana edificó un altar al pie del monte, y doce columnas, según las doce tribus de Israel."</em><br/>
<br/>A lo largo del Pentateuco se menciona insistentemente que Jehová le dio la ley a Moisés, ejemplo:<br/><br/>
Éxodo 25:1 <em>"Jehová habló a Moisés, diciendo:"</em><br/><br/>
Levítico 1:1 <em>"Llamó Jehová a Moisés, y habló con él desde el tabernáculo de reunión, diciendo:"</em>
<br/><br/>
<span class="incisos">2. Evidencias externas:</span><br/><br/>
El Antiguo Testamento reconoce a Moisés como el autor del Pentateuco. Por ejemplo: Josue 1:7-8, Esdras 6:18, Nehemías 8:1, entre muchos otros.<br/><br/>
Por otro lado el Nuevo Testamento también señala a Moisés como autor de Pentateuco como lo muestran los versículos Marco 10:3-5, Juan 1:17, entre otros.<br/><br/>
El historiador judío Flavio Josefo, mencionaba que en la época de Cristo los judíos reconocían a Moisés como el autor de la ley. Tanto Jesús como los evangelistas atribuyen el Pentateuco a Moisés.
<br/><br/>
El profesor A. S. Yahuda presenta argumentos a favor de la autoría de Moisés, señalando que a lo largo del Pentateuco se presenta una influencia egipcia en el lenguaje y forma de pensar, lo cual, solo se puede explicar si el autor es Moisés.<br/><br/>
<span class="incisos">3. Otros argumentos de los críticos (Parte 1):</span><br/><br/>
A. Retomando brevemente lo expuesto al final del punto anterior, es necesario señalar que la afirmación que señala que en los tiempos de Moisés el arte de la escritura contaba con muy poca difusión fue uno de los principales fundamentos iniciales de las teorías de los críticos que negaron la autoría de Moisés sobre el Pentateuco. El paso del tiempo y los hallazgos arqueológicos fueron pieza por pieza derrumbando el argumento.
Eric Lyons en su artículo La autoría de Moisés sobre el Pentateuco- Puesta a prueba y probada hace la siguiente afirmación:
“Desde el periodo más temprano del desarrollo de la hipótesis documental, se asumía que Moisés había vivido en una época anterior al conocimiento de la escritura. Uno de los “padres fundadores” de esta teoría, Julius Wellhausen, estaba convencido de que “el antiguo Israel ciertamente no vivía sin bases dadas por Dios para ordenar la vida humana; solamente no estaban plasmadas de manera escrita” (1885, p. 393, emp. Added). Sólo trece años después, Hermann Schultz declaró: “Sobre el carácter legendario de los narradores premosáicos, el tiempo que tratan es prueba suficiente. Era un tiempo anterior a todo conocimiento sobre la escritura (1898, pp. 25-26, emp. added).”<a title="Lyons, Eric. Mosaic athorship of the Pentateuch—Tried and True. Inspiration of the Bible. Apologetics Press. USA. 2013. PDF Recuperado el 4 de Junio de 2013. URL: http://www.apologeticspress.org/apcontent.aspx?category=13&article=36"><span class="textpie">(2)</span></a><br/><br/>

Lamentablemente para la hipótesis documental la arqueología comenzó a presentar evidencia en su contra, Lyons cita las siguientes:
<br/><br/>
“1. En 1949 C. F. A Schaeffer “encontró una tableta en Ras Shamra que contenía las treinta letras del alfabeto ugaríctico en su orden propio. Fue descubierto que la secuencia del alfabeto ugaríctico era la misma que la del hebreo moderno, revelando que el alfabeto hebreo va por lo menos 3500 años atrás” (Jacson, 1982, p. 32. added).
<br/><br/>
2. En 1933, J. L. Starkey, quien había estudiado bajo el afamado arqueólogo W. M. F. Petrie, excavó la ciudad de Laquis, que tuvo un rol prominente en la conquista de Josué sobre la tierra de Canaán (Josué 10). Entre otras cosas, él desenterró una jarra de cerámica “con una dedicación escrita con once letras arcaicas, la inscripción hebrea más antigua conocida” (Wiseman, 1974, p. 705).
<br/><br/>
3. En 1901-1902, el código de Hammurabi fue descubierto en la antigua ciudad de Susa (en lo que es actualmente Irán) en una expedición arqueológica francesa bajo la dirección de Jacques Morgan. Estaba escrito en una pieza de diorita negra de casi ocho pies de altura y contenía 282 secciones. En su libro, Arqueología y la historia de la Biblia, Joseph Free y Howard Vos declaran:
El código de Hammurabi fue escrito varios cientos de años antes del tiempo de Moisés (c. 1500-1400 a. C.)… Este código, del periodo 2000-1700 a. C., contiene leyes avanzadas similares a las leyes mosáicas… a la vista de la evidencia arqueológica, la crítica destructiva no puede insistir más en que las leyes de Moisés eran demasiado avanzadas para su tiempo (1992, pp. 103, 55. emp. added).”<a title="Ibid."><span class="textpie">(3)</span></a><br/><br/>
Con lo anterior podemos ver que la escritura estaba bien desarrollada y establecida en el tiempo en el que Moisés vivió, por lo cual, no se puede alegar que él no tuvo contacto con la escritura.
<br/><br/>
B. Los críticos señalan que el hecho de que en los pasajes patriarcales se mencione que estos poseían camellos implica un anacronismo que es prueba suficiente para rechazar la autoría de Moisés. En el Génesis encontramos que Abraham poseía camellos, también hace referencia a la existencia de camellos en Egipto, entre otras menciones. El primer libro de la Biblia señala claramente que en el tiempo de los patriarcas los camellos ya habían sido domesticados.
Lo anterior ha sido uno de los argumentos favoritos de los críticos, quienes afirman que los camellos fueron domesticados varios siglos después del tiempo de Moisés.
Entre estos críticos encontramos a Finkelstein y a Silberman, quienes mencionan lo siguiente:<br/><br/>

“Ahora por medio de la investigación arqueológica sabemos que los camellos no fueron domesticados como bestias de carga antes que a los finales del segundo milenio y no fueron utilizados ampliamente en esa capacidad en el antiguo cercano oriente hasta después del 1000 antes de la era común (2001, p. 37, emp. added)”<a title="Ibid."><span class="textpie">(4)</span></a><br/><br/>
De esta manera encontramos que los críticos y los teólogos liberales afirman que era imposible que Abraham poseyese camellos, ya que simplemente no habían sido domesticados, por lo que lo más probable era que estos animales le fueran extraños.<br/><br/>
Lyons nos señala que estos autores han formulado sus argumentos del silencio, sin tener una verdadera base arqueológica para fundamentarlos, además hace la siguiente declaración incluyendo referencias a hallazgos arqueológicos que nos muestras que los camellos ya habían sido domesticados en los tiempos de Abraham:<br/><br/>
“Lo que hace a sus afirmaciones más irritantes es que existen varias piezas (y han existido desde hace tiempo) que prueban que los camellos fueron domesticados durante (e incluso antes) del tiempo de Abraham (aproximadamente el 2000 a. C.). En un artículo que apareció en Journal of Near Eastern Studies, hace medio siglo, el profesor Joseph Free enumeró varios ejemplos de hallazgos arqueológicos egipcios que apoyaban la domesticación de los camellos. (NOTA: Las fechas dadas a las dinastías son de Clayton, 2001, pp. 14-68). Las evidencias más recientes vienen de una cabeza de camello de cerámica y una tableta de terra cotta en la que hay un hombre montando un camello. De acuerdo con Free, ambos son del Egipto predinástico (1944, pp. 189-190), lo que de acuerdo a Clayton es aproximadamente antes del 3150 a. C. Free también enumera tres cabezas de camello y una vasija de piedra caliza en la forma de un camello recostado- todas fechadas durante la primera dinastía de Egipto (3050-2890 a. C.), y  un petroglifo de la sexta dinastía (2613-2498 a. C.) representando a un hombre y a un camello. Dichas evidencias han llevado a un respetable Egiptólogo a concluir que “la evidencia existente claramente indica que el camello doméstico era conocido (en Egipto- EL) para el 3000 a. C.” –mucho antes del tiempo de Abraham (Kitchen, 1980, 1:228).<br/><br/>
Probablemente el hallazgo más convincente que apoya la domesticación temprana de los camellos en Egipto es una cuerda hecha de cabello de camello encontrada en el Fayum (un oasis en el área del sudoeste del moderno Cairo). Las dos hebras de cabello torcidas que miden un poco más de tres pies, fueron encontradas a finales de los 1920s, y fueron enviadas al Museo Natural de Historia, donde fueron analizadas y comparadas con el cabello de varios animales diferentes. Después de extenuantes pruebas, se determinó que era cabello de camello, dándole una fecha (analizando el estamento en el que fue encontrado) dentro de la tercera o cuarta dinastía egipcia (2686-2498 a. C.). En su artículo, Free también enumera otros descubrimientos de alrededor del 2000 a. C. y posteriores, cada uno de los cuales muestra la domesticación de los animales (1944, pp. 189-190).<br/><br/>
Prolíficos en Egipto, los hallazgos relativos a la domesticación de camellos no se limitan únicamente al continente Africano. En su libro, El antiguo oriente y el Antiguo Testamento, Kenneth Kitchen, profesor emeritus de la Universidad de Liverpool, reportó varios descubrimientos encontrados fuera de Egipto, probando que la antigua domesticación de camellos existía cerca del 2000 a. C. Listas léxicas de Mesopotamia han sido reveladas, las cuales, muestran el conocimiento de camellos domesticados tan antiguas como la fecha mencionada. Huesos de camello han sido encontrados dentro de habitaciones familiares en las ruinas de Mari en la actual Siria, los analistas de fósiles creen que poseen por lo menos 4000 años. Aun más, textos sumerios del tiempo de Abraham han sido descubiertos en la antigua ciudad de Nippur (localizada en lo que ahora es el sudeste de Iraq) que claramente implican la domesticación de camellos por su alusión a la leche de camello (Kitchen, 1966, p. 79)”<a title="(El presente artículo se realizó gracias a la autorización otorgada por Apologetics Press para hacer traducciones del artículo Mosaic athorship of the Pentateuch—Tried and True. Cuyo autor es Eric Lyons. Quien desee indagar más sobre el tema puede visitar la página www.apologeticspress.org donde podrá encontrar el artículo original y más relativos al tema.)"><span class="textpie">(5)</span></a> <br/><br/>
