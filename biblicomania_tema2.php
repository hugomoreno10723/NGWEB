          <p class="textContenido">
<span class="incisos">3. Otros argumentos de los críticos (Parte 2):</span><br/><br/>
C. En los círculos de la crítica bíblica también se suele esgrimir que la mención de los filisteos en los tiempos de los patriarcas constituye un error histórico, que descalifica a Moisés como el autor del Pentateuco.<br/><br/>

Lyons nos presenta una compilación breve de algunos comentarios críticos sobre el tema:
<br/><br/>
“<em>Los filisteos… no llegaron a palestina sino hasta después del tiempo de Moisés” (Gottwald, 1959, p. 104, emp. added), y cualquier mención de ellos antes de este tiempo representa una “imprecisión histórica” (Frank, 1964, p. 323). De esta forma como concluye Millar Burrows, la mención de los filisteos en Génesis debe de ser considerado “un conveniente e inofensivo anacronismo”, el cual “es sin duda alguna un error” (1941, p. 277).</em>”<a title="Lyons, Eric. Mosaic athorship of the Pentateuch—Tried and True. Inspiration of the Bible. Apologetics Press. USA. 2013. PDF Recuperado el 4 de Junio de 2013. URL: http://www.apologeticspress.org/apcontent.aspx?category=13&article=36"><span class="textpie">(1)</span></a>
<br/><br/>
El Pentateuco efectivamente menciona a los filisteos en repetidas ocasiones, incluso en el tiempo de Abraham e Isaac, como lo podemos constatar en Génesis 21:32-34 y en Génesis 26. Pero esto no necesariamente implica la existencia de una imprecisión histórica o de un error dentro de la Escritura.
<br/><br/>
Lyons señala que los siguiente al respecto:
<br/><br/>
“Como en muchas de las acusaciones en contra de la Escritura, aquellos que reclaman que la nación filistea no se encontraba en los días de Abraham están basando su conclusión en un por lo menos una suposición improbable- a saber, que los filisteos que vivían en los días de los patriarcas eran una gran nación, similar a la que vivió durante el tiempo del reino unido. La evidencia de cualquier forma sugiere que esta suposición es equivoca. La Biblia no presenta a los filisteos de los días de Abraham como la misma nación poderosa de los filisteos que se levantaría cientos de años después. Abimelech, el rey de Gerar, es retratado como siendo intimidado por Abraham (cf. Génesis 21:25). Seguramente de haber sido los filisteos una poderosa nación en el tiempo de los patriarcas, ellos no hubieran tenido miedo de un hombre (Abraham) y de unos cientos de siervos (cf. Génesis 14:14). Aún más, ninguna de las cinco grandes ciudades Estado que fueron tan prominentes a lo largo del periodo de los jueces y del reino unido (Ashod, Ascalón, Ecrón, Gat y Gaza- Josué 13:3; 1. Samuel 6:17) es mencionada. Por el contrario, solo una pequeña aldea conocida como Gerar es mencionada. El asumir que la Biblia señala a la civilización filistea de forma completa y desarrollada como presente durante los días de Abraham es un error.”<a title="Ibid."><span class="textpie">(2)</span></a> <br/><br/>

Para fundamentar su argumento Lyons introduce en su artículo los siguientes comentarios de expertos:<br/><br/>
Haciendo un comentario sobre los filisteos, Larry Richards observó:<br/><br/>
Mientras hay un consenso general de que el asentamiento masivo de la costa de Canaan por los pueblos marítimos de Creta tuvo lugar cerca del 1200 a. C., no hay razón para suponer que no existían asentamientos filisteos mucho tiempo antes de esta fecha. En el tiempo de Abram al igual que en el tiempo de Moisés una gran cantidad de pueblos se habían establecido en Canaan, incluyendo Hititas del lejano norte. Ciertamente los pueblos marinos que surcaban el Mediterráneo habían establecido colonias a lo largo de las costas de la cuenca entera siglos antes del tiempo de Abraham. No hay razón para suponer que los filisteos, cuyos ancestros venían de Creta, no se encontraban entre ellos (1993, p. 40).<br/><br/>
No existe evidencia arqueológica que niegue que varios grupos de “los pueblos marinos” estuvieron en Canaán mucho tiempo antes de la llegada del cuerpo principal a inicios del siglo XII a. C. (ver Unger, 1954, p. 91)… Asumir que ningún grupo de filisteos vivió en Palestina durante el tiempo de Abraham porque la arqueología no los ha documentado sino hasta cerca del 1190 a. C. es argumentar por medio de evidencia negativa, y esto no tiene peso substancial. En respuesta a aquellos que niegan la existencia de los filisteos basados en su silencio dentro del mundo arqueológico antes de este tiempo, encontramos que el profesor Kitchen afirmó:<br/><br/>

“<em>De manera inscripcional, sabemos muy poco sobre los pueblos egeos comparados con el resto de lo pueblos del Antiguo Cercano Oriente del segundo milenio a. C., por esto es prematuro negar completamente la posible existencia de filisteos en el egeo antes del 1200 a. C. (1966, p. 80n).</em>”<a title="Ibid."><span class="textpie">(3)</span></a> <br/><br/>

Por último tomaremos un caso más manejado por Lyons. Nuestro autor señala que críticos como McKinsey elaboran argumentos como el siguiente para desacreditar la autoría del Moisés sobre el Pentateuco citando: <br/><br/>

Deut. 15:22 dice, “<em>Lo comerás dentro de tus puertas.</em>” La frase “<em>dentro de tus puertas</em>” aparece en el Pentateuco cerca de veinticinco veces y refiere a las puertas de las ciudades palestinas, las cuales no fueron habitadas por los israelitas hasta después de la muerte de Moisés (1995, p. 363, emp. en orig.).<a title="Ibid."><span class="textpie">(4)</span></a> <br/><br/>

Lyons nos señala que al hacerse este tipo de declaraciones se comete un grave error asumiendo que el pasaje se refiere únicamente a las “<em>puertas de las ciudades palestinas</em>”. Nuestro autor de igual forma señala que el error de estos escépticos es el no mencionar que de hecho la expresión “<em>la puerta</em>” o “<em>las puertas</em>”, no siempre hace referencia al acceso al lugar donde se estableció una población; por ejemplo Éxodo 32:26 señala “<em>se puso Moisés a la puerta del campamento, y dijo: ¿Quién está por Jehová? Júntese conmigo. Y se juntaron con él todos los hijos de Leví.</em>” Es claro que en este pasaje “<em>la puerta</em>” de ninguna manera refiere a una construcción como la de una ciudad, ya que los israelitas en este momento habitaban en tiendas formando un campamento, de esta manera vemos que este término puede referir tanto a la entrada de una ciudad como a la entrada de una tienda, habría que examinar cada caso.<br/><br/>

Ahora el que se utilice el término “<em>puerta</em>” para hacer alusión al acceso de una ciudad palestina en realidad no debe de generar problema alguno, pues recordemos que Moisés sabía que Dios les habría de entregar las ciudades de la tierra prometida, como nos lo muestra Éxodo 3:7-8 que señala:<br/><br/>

“<em>7 Dijo luego Jehová: Bien he visto la aflicción de mi pueblo que está en Egipto, y he oído su clamor a causa de sus exactores; pues he conocido sus angustias, 8 y he descendido para librarlos de mano de los egipcios, y sacarlos de aquella tierra a una tierra buena y ancha, a tierra que fluye leche y miel, a los lugares del cananeo, del heteo, del amorreo, del ferezeo, del heveo y del jebuseo.</em>”<br/><br/>

Con lo anterior podemos ver que Moisés al hablar sobre las puertas de las puertas de las ciudades palestinas está dando por hecho que el Señor se las habría de dar como Él lo había prometido.<br/><br/>

Podríamos abundar más sobre este tema pues la discusión con los escépticos es bastante amplia, pero creo que a quien le interese profundizar más sobre el tema puede consultar a expertos en el tema. En las páginas <a href="http://www.creation.com" target="_blank"><strong>www.creation.com</strong></a> y <a href="http://www.apologeticspress.org" target="_blank"><strong>www.apologeticspress.org</strong></a> se puede encontrar varios artículos, y de igual manera encontrarán bibliografía de consulta (especialmente en la página de creation se pueden encontrar varios artículos sobre la fiabilidad de la cronología bíblica, quien tenga interés por este tema puede consultar el siguiente artículo <a href="http://creation.com/fall-of-the-sothic-theory-egyptian-chronology-revisited" target="_blank"><strong>http://creation.com/fall-of-the-sothic-theory-egyptian-chronology-revisited</strong></a>). También la obra de Josh McDowell “<em>Evidencias que demandan un veredicto</em>” puede dar una buena introducción al tema. <br/><br/>

